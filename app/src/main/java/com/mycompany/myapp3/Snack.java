package com.mycompany.myapp3;

public class Snack
{
	private int x;
	private int y;
	public Snack(int x, int y)
	{
		this.x = x;
		this.y = y;
	}
	public int getx()
	{
		return x;
	}
	public int gety()
	{
		return y;
	}
	public void setx(int x)
	{
		this.x = x;
	}
	public void sety(int y)
	{
		this.y = y;
	}
}
